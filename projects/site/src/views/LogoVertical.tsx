let logoScales = {
  small: 0.7,
  medium: 0.8,
  large: 0.9,
}

export const LogoVertical = memo(
  ({ size, ...rest }: ViewProps & { size?: 'small' | 'medium' | 'large' }) => {
    const theme = useTheme()
    const screenSize = useScreenSize()
    const parallax = useParallax()
    const nav = useNavigation()
    const scale = logoScales[size || screenSize]

    return (
      <View
        position="relative"
        zIndex={100000}
        cursor="pointer"
        alignItems="center"
        justifyContent="center"
        userSelect="none"
        transform={{ scale }}
        padding={[0, 35]}
        onClick={async e => {
          e.preventDefault()
          if ((await nav.getRoute()).url.pathname === '/') {
            parallax && parallax.scrollTo(0)
          } else {
            nav.navigate('/')
          }
        }}
        {...rest}
      >
        <BrandMark id="orbit-mark" />
        <Space size="lg" />
        <BrandWords fill={theme.color} width={102 * 0.8} height={23 * 0.8} />
      </View>
    )
  },
)

